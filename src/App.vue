      <h2>Conclusion</h2>
      <hr class="titlebar" />

      <p>
        The revenue shortfalls projected in this analysis, a combined total of
        $344M in the moderate scenario and $647M in the severe scenario for FY20
        and FY21, represent an unprecedented fiscal challenge for the City.
        While the exact economic impact remains unknown, it is clear that the
        current emergency will have an enormous impact on the City’s finances at
        a time when a historic number of residents are unemployed and the need
        for City services has never been greater. As the City faces these
        challenges, it is more important than ever that the City operates
        efficiently and effectively and is truly able to do more with less. As
        the City perseveres through the current crisis, the Controller’s Office
        remains committed to these goals and ensuring that the City and its
        residents make it through this crisis stronger than ever.
      </p>
