        are:
      </p>

      <ul>
        <li>
          <b>Moderate Impact</b>: A more limited scenario resulting from a relatively
          short-term recession in which the economy experiences a brief,
          six-month shock and recovers close to pre-crisis levels by the first
          quarter of calendar year 2021. While uncertain, this scenario would be
          more likely if the city’s stay-at-home order is relatively short,
          lasting between 8 and 10 weeks.
