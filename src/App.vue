      <p>
        The COVID-19 pandemic is having an unprecedented impact on the
        Philadelphia region. Following the announcement of Philadelphia’s first
        confirmed case on March 10, schools were closed on March 16 and a
        stay-at-home order began on March 23. The order imposed a number of
        public health restrictions on the city’s residents designed to slow the
        spread of infections and prevent the crisis from overwhelming the city’s
        healthcare capacity. These restrictions have had an immediate and
        profound effect on the local economy, with non-essential businesses
        forced to close temporarily and sudden and enormous job losses. While
        not yet reflected in official statistics, the reality of the impact so
        far indicates the city is in a recession. As the economy contracts, the
        City’s tax revenues will decline, particularly those most sensitive to
        economic conditions, such as wage and sales taxes.
      </p>

      <p>
        This report provides an estimate of the potential impact of the COVID-19
        crisis on the City’s revenues over the next year. Like other cities
        across the country, a great deal of uncertainty surrounds Philadelphia’s
        future from both a public health and economic perspective. The ultimate
        economic toll of the COVID-19 pandemic will depend on a range of items,
        including the success of public health efforts, the federal government’s
        policy response, and the ability to safely re-open the city’s economy.
        Given this uncertainty, the Controller’s Office analyzed two possible
        scenarios, moderate and severe, and their associated tax revenue
        impacts. The impact is presented relative to projections in the City’s
        <a
