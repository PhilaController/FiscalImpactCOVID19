          target="blank_"
        >FY21-FY25 Five Year Financial Plan</a>, as proposed on
        March 5. The projections examine the short-term impact to the City’s
        finances, focusing on fiscal year 2020 (FY20), which ends on June 30,
        2020, and fiscal year 2021 (FY21).
      </p>

      <p>
        Relative to the March baseline, the Controller’s Office estimates a
        combined tax revenue shortfall in fiscal years 2020 and 2021 ranging
        from $344M (-4.0 percent) in the moderate scenario to $647M (-7.5
