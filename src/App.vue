      <h2>Tax Revenue Impacts</h2>
      <hr class="titlebar" />

      <p>
        The economic impact of COVID-19 will affect the city’s taxes
        differently. For example, wage, BIRT, and sales taxes are assumed to be
        more severely impacted than the real estate tax due to suppressed
        business activity and an increase in unemployment in the Philadelphia
        region. In addition to economic effects, the City moved its BIRT and net
        profits tax filing dates from April to July to match federal changes,
        shifting hundreds of millions in tax revenue from FY20 to FY21. The
        following sections summarize the projections for each of the City’s
        major taxes.
      </p>

      <!-- Wage -->
      <h3>Wage & Earnings Tax</h3>
      <p>
        The City’s wage tax is its single largest tax revenue source
        <a
          href="#ref8"
          id="fn8"
          title="Jump back to footnote 8 in the text."
        >[8]&nbsp;</a> and is
        particularly sensitive to unemployment in the city. With job losses
        reaching historic levels, a large contraction in the City’s wage tax is
        expected in the short term. The largest contractions are expected in the
        restaurants, leisure and hospitality, and retail sectors. In fiscal year
        2019, these sectors accounted for about 12 percent of the City’s total
        wage tax collections. The City’s health and social services sector
        accounted for the largest fraction (21 percent), which should help
        mitigate the revenue losses to some degree. Job losses in this sector
        are expected to be less severe, although the industry has already seen
        impacts from the cancellation of non-essential and elective services
        during the stay-at-home order.
        <a
          href="#ref9"
          id="fn9"
          title="Jump back to footnote 9 in the text."
        >[9]&nbsp;</a>
      </p>

      <p>
        Importantly, the analysis presented here does not account for wage tax
        refunds for non-resident employees who were forced to work from home
        during the stay-at-home order. With non-residents representing about 40
        percent of the wage tax base, the City could see additional declines in
        its wage tax collections in FY21, as large as $30M for a stay-at-home
        order that lasts through June 2020.
        <a
          href="#ref10"
          id="fn10"
          title="Jump back to footnote 10 in the text."
        >[10]&nbsp;</a> However, the impact remains highly
        uncertain at this time and should be treated as an illustration of
        potential scale, rather than an exact forecast.
      </p>

      <!-- Sales -->
      <h3>Sales Tax</h3>
      <p>
        With the majority of the city’s businesses closed, the retail and
        restaurant industries are experiencing immediate and substantial losses.
        These two industries account for the largest portions of the City’s
        sales tax revenue, with retail accounting for 32 percent and restaurants
        accounting for 15 percent in FY18. As such, the analysis assumes a drop
        in sales tax collections for these sectors of 50 percent in the moderate
        scenario and 70 percent in the severe scenario during the fourth quarter
        of FY20. Collections are assumed to decline by 30 percent (moderate) and
        50 percent (severe) for other sectors. Sales tax revenue improves
        quarter by quarter, with the recovery delayed by a quarter in the severe
        case relative to the moderate scenario.
      </p>

      <!-- Business Taxes -->
      <h3>Business Taxes</h3>
      <p>
        Business tax collections for tax year 2019, typically due in April 2020,
        are not expected to be widely affected by the current crisis, with
        larger economic effects appearing in FY21 (April 2021). The City shifted
        its filing dates for BIRT and the net profits tax from April 2020 in
        FY20 to July 2020 in FY21. As the amount of businesses who will pay
        these taxes early remains uncertain, the projections assume the entirety
        of the revenue from April will be shifted to July in order to gauge the
        maximum effect on the City’s short-term cash flow. For BIRT, this
        amounts to about $380M moving from FY20 to FY21.
      </p>
      <p>
        To estimate the economic effects on BIRT and net profits collections,
        the analysis assumes a 5 percent (moderate) and 10 percent (severe)
        decline from baseline for July 2020 collections and a larger 10 percent
        (moderate) and 15 percent (severe) decline for the April 2021. These
        reductions are consistent with the magnitude of revenue declines in the
        years following the Great Recession.
      </p>
      <p>
        However, it is worth noting that BIRT collections are particularly
        challenging to forecast due to a number of policy and accounting
        complexities. For example, while businesses are typically required to
        pre-pay their tax liability for the current tax year based on the
        previous year’s liability, the regulations allow companies to pre-pay a
        lower amount if there is reason to believe the current year’s tax
        liability will be lower.
        <a
          href="#ref11"
          id="fn11"
          title="Jump back to footnote 11 in the text."
        >[11]&nbsp;</a> To the extent that more businesses use this
        option than expected, additional revenue could shift from July 2020 to
        April 2021.
      </p>

      <!-- Beverage Tax -->
      <h3>Beverage Tax</h3>
      <p>
        Similar to the sales tax, the COVID-19 crisis is expected to negatively
        impact beverage tax collections due to effects on the restaurant and
        dining industry. The analysis projects a 20 percent (moderate) and 40
        percent (severe) decline from baseline in FY20 Q4, with the recovery
        delayed by a quarter in the severe case. With restaurants expected to be
        one of the last sectors to fully re-open, these effects are projected to
        last through the end of FY21.
      </p>

      <!-- Parking Tax -->
      <h3>Parking Tax</h3>
      <p>
        Vehicle traffic has declined sharply during the stay-at-home order,
        <a
          href="#ref12"
          id="fn12"
          title="Jump back to footnote 12 in the text."
        >[12]&nbsp;</a>
        leading to lower estimates for parking tax collections. Collections are
        projected to decline by 30 percent (moderate) and 50 percent (severe)
        from the baseline projections in FY20 Q4. With social distancing
        measures likely to continue into 2021 and the City’s tourism industry
        adversely affected by the pandemic, declines are projected to continue
        through the end of FY21.
      </p>

      <!-- Amusement Tax -->
      <h3>Amusement Tax</h3>
      <p>
        The COVID-19 pandemic is expected to have a large effect on the City’s
        amusement tax, which is imposed on admission fees to events like
        concerts, movies, athletic events, and night clubs. The City’s current
        social distancing measures prohibit such events, and even if those
        measures are relaxed, the projections assume residents will largely shy
        away from large gatherings for some time. Collections are projected to
        decline by 70 percent (moderate) and 90 percent (severe) from the
        baseline projections in FY20 Q4, with large reductions continuing
        through FY21.
      </p>

      <!-- Realty Transfer Tax -->
      <h3>Realty Transfer Tax</h3>
      <p>
        With the City’s housing market remaining active in recent years, revenue
        from the tax on residential and commercial real estate transfers has
        been historically high. In fact, March’s collections total of $47M was a
        record high. Unlike the Great Recession, the current crisis is not
        housing-driven, making the effects of the pandemic on transfer tax
        revenue particularly uncertain. In both scenarios considered, the
        projections assume a 30 percent drop in collections in FY20 Q4, driven
        by the stay-at-home order and declines of 5 (moderate) to 10 percent
        (severe) from baseline projections continuing into 2021 due to ongoing
        social distancing measures. These declines are consistent with early
        indicators of the impact of the stay-at-home order on Philadelphia’s
        housing market.
        <a
          href="#ref13"
          id="fn13"
          title="Jump back to footnote 13 in the text."
        >[13]&nbsp;</a>
      </p>

      <!-- Real Estate Tax -->
      <h3>Real Estate Tax</h3>
      <p>
        The analysis assumes no impact on the City’s real estate tax collections
        in FY20 or FY21. Real estate tax collections are based on assessments
        that are released about a year prior to the filing date. As such, the
        economic impact on the tax will be delayed. As the current crisis begins
        to affect the City’s housing market, the City’s property tax collections
        would be first impacted in FY22, which are based on assessments released
        in April 2021.
      </p>
