        the City’s current stay-at-home order on the local economy. In both
        scenarios, the economic effects persist throughout calendar year 2021 as
        unemployment and local businesses slowly recover to pre-crisis levels.
        Moving forward, the Controller’s Office will continue to closely monitor
        policy responses and public health forecasts, particularly the
        possibility of a second wave of infections, and will update the
        projections in this analysis in the coming months.
      </p>
