        recent forecasts predicting a global recession with a recovery that
        could last through 2021.
        <a
          href="#ref3"
          id="fn3"
          title="Jump back to footnote 3 in the text."
        >[3]&nbsp;</a> Both the moderate and severe scenarios in this
